double InsertFaults (double Signal, tFIU* FIU)		// Funktion durchläuft alle aktivierten Fehlerblöcke in einer sinnvollen Reihenfolge (siehe Präsentation)
{
	double ModifiedSignal = Signal;
	

	if (FIU->Freeze.Enable)
	{
		ModifiedSignal = FIU->Freeze.LastValue;
	}
	else
	{
		FIU->Freeze.LastValue = Signal;
	}


	if (FIU->FixedValue.Enable)
	{
		ModifiedSignal = FIU->FixedValue.Value;
	}


	if (FIU->Delay.Enable)
	{
		if (FIU->Delay.Time / SAMPLETIME > History.size()) // Es ist noch nicht genug Zeit verstrichen um auf einen x Sekunden zurückliegenden Wert zurückgreifen zu können -> also wird nur Null als Signal weitergeleitet
		{
			History.push_back(Signal);
			ModifiedSignal = 0;
		}
		else 
		{								// Hier sind jetzt genug Daten in der History vorhanden und somit können diese genutzt werden
			History.push_back(Signal);
			ModifiedSignal = History.front();		// Erstes Element der Liste wird nun verwendet
			History.pop_front();					// und daraufhin aus der Liste entfernt
		}
	}


	if (FIU->Negate.Enable && FIU->Typ == DIGITAL)
	{
		ModifiedSignal = !ModifiedSignal;
	}


	if (FIU->Drift.Enable && FIU->Typ == CONTINUOUS)
	{
		FIU->Drift.Timer += SAMPLETIME;
		if (FIU->Drift.Timer > FIU->Drift.Time)
			ModifiedSignal += FIU->Drift.Value;	// Maximaler Wert für das Abdriften
		else
			ModifiedSignal += (FIU->Drift.Value * (FIU->Drift.Timer / FIU->Drift.Time)); // Mit der Zeit steigender Drift-Wert
	}


	if (FIU->Offset.Enable && FIU->Typ == CONTINUOUS)
	{
		ModifiedSignal += FIU->Offset.Value;
	}


	if (FIU->Slewrate.Enable && FIU->Typ == CONTINUOUS)
	{
		if ((Signal - FIU->Slewrate.LastValue) > (FIU->Slewrate.MaxSlewrate * SAMPLETIME))
			ModifiedSignal += (FIU->Slewrate.MaxSlewrate * SAMPLETIME);
		else if ((Signal - FIU->Slewrate.LastValue) < -(FIU->Slewrate.MaxSlewrate * SAMPLETIME))
			ModifiedSignal -= (FIU->Slewrate.MaxSlewrate * SAMPLETIME);
		else
			ModifiedSignal = ModifiedSignal;
		FIU->Slewrate.LastValue = Signal;
	}
	else
	{
		FIU->Slewrate.LastValue = Signal;
	}


	if (FIU->Noise.Enable && (FIU->Typ == CONTINUOUS || FIU->Typ == DIGITAL))
	{
		if (noise_array_counter >= NOISE_ARRAY_SIZE)	// Nach dem vollständigen Durchlaufen aller Rauschwerte wird das "Muster" wiederholt.
			noise_array_counter -= NOISE_ARRAY_SIZE;
		ModifiedSignal += FIU->Noise.Amplitude * noise_array[noise_array_counter];

		noise_array_counter += FIU->Noise.Pattern;
	}

	
	return ModifiedSignal;
}


